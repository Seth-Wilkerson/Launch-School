/*
  input;
    item identifier
    array of transaction objects
  output:
    array of transactions for that inventory item

  need to to analaze each transaction object
  and perform selection to filter down to only the ones with specified ID

  implementation detail
    use filter to iterate
    and select items with matching id
*/

function transactionsFor(itemId, transactions) {
  return transactions.filter(transObj => {
    return itemId === transObj['id'];
  });
}


